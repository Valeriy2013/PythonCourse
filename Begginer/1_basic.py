num1 = int(input("Insert first number: "))

num2 = int(input("Insert second number: "))

add = num1 + num2
sub = num1 - num2
mult = num1 * num2
div = num1 / num2
pow = num1 ** num2

print("Add result: " + str(add))
print("Sub result: " + str(sub))
print("Multiply result: " + str(mult))
print("Division result: " + str(div))
print("Power of result: " + str(pow))
